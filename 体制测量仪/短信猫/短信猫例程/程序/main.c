#include<reg51.h>

unsigned char PuZh[] = "DX************中国海洋大学自动化";		//*处为11位手机号码

//--声明全局函数-//
void UsartConfiguration();
void Delay10ms(unsigned int c);   //误差 0us

/*******************************************************************************
* 函 数 名         : main
* 函数功能		   : 主函数
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/

void main()
{
	unsigned char i;
	UsartConfiguration();
	while(1)
	{  
		for(i=0; i<50; i++)			//可以自己改为合适的值，
		{
			SBUF = PuZh[i];   //将要发送的数据放入到发送寄存器
			while(!TI);		  //等待发送数据完成
			TI=0;			  //清除发送完成标志位
		}
		Delay10ms(50);		  //延时一下再发
		TR1=0;
	
	}
}
/*******************************************************************************
* 函 数 名         :UsartConfiguration()
* 函数功能		   :设置串口
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/

void UsartConfiguration()
{
	SCON=0X50;			//设置为工作方式1
	TMOD=0X20;			//设置计数器工作方式2
	TH1=0XFd;		    //计数器初始值设置，注意波特率是9600的
	TL1=0XFd;
	TR1=1;					    //打开计数器
}

/*******************************************************************************
* 函 数 名         : Delay10ms
* 函数功能		   : 延时函数，延时10ms
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/

void Delay10ms(unsigned int c)   //误差 0us
{
    unsigned char a, b;

	//--c已经在传递过来的时候已经赋值了，所以在for语句第一句就不用赋值了--//
    for (;c>0;c--)
	{
		for (b=38;b>0;b--)
		{
			for (a=130;a>0;a--);
		}          
	}       
}